const storedLanguage = (() => {
  if (typeof window === 'undefined') {
    return 'en';
  }
  try {
    return localStorage.getItem('tx5dr-site:language') || undefined;
  } catch {
    return undefined;
  }
})();

void i18n.use(initReactI18next).init({
  resources,
  lng: storedLanguage || undefined,
  fallbackLng: 'en',
  interpolation: {
    escapeValue: false,
  },
});

export default i18n;
